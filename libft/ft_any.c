#include "libft.h"

int	ft_any(size_t **tab, size_t (*f)(size_t *))
{
	size_t i;

	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			return (1);
		i++;
	}
	return (0);
}
