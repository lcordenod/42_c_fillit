#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int a;

	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	a = nb;
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else
	{
		ft_putchar(a + '0');
	}
}
